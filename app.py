def detect_intent_multilingual(user_message):
    """Detect user intent from multilingual input"""
    message_lower = user_message.lower()
    
    # Scheme keywords
    scheme_keywords = ['scheme', 'subsidy', 'government', 'yojana', 'benefit', 'pm kisan', 
                      'insurance', 'loan', 'credit', 'pension', 'рдпреЛрдЬрдирд╛', 'рд╕рдмреНрд╕рд┐рдбреА', 'рдмреАрдорд╛',
                      'rkvy', 'kusum', 'solar', 'fasal bima', 'kisan credit', 'organic']
    
    disease_keywords = ['disease', 'sick', 'infected', 'problem', 'leaf', 'рд░реЛрдЧ', 'рдмреАрдорд╛рд░реА']
    price_keywords = ['price', 'cost', 'market', 'rate', 'mandi', 'рдХреАрдордд', 'рджрд╛рдо', 'рдмрд╛рдЬрд╛рд░']
    weather_keywords = ['weather', 'temperature', 'rain', 'climate', 'рдореМрд╕рдо', 'рддрд╛рдкрдорд╛рди']
    crop_keywords = ['wheat', 'rice', 'crop', 'farming', 'cultivation', 'grow', 'рдЧреЗрд╣реВрдВ', 'рдЪрд╛рд╡рд▓', 'рдлрд╕рд▓']
    greeting_keywords = ['hello', 'hi', 'hey', 'namaste', 'start', 'рдирдорд╕реНрддреЗ', 'рд╣реИрд▓реЛ']
    
    # Check intents
    if any(keyword in message_lower for keyword in scheme_keywords):
        return 'scheme'
    if any(keyword in message_lower for keyword in disease_keywords):
        return 'disease'
    if any(keyword in message_lower for keyword in price_keywords):
        return 'price'
    if any(keyword in message_lower for keyword in weather_keywords):
        return 'weather'
    if any(keyword in message_lower for keyword in crop_keywords):
        return 'crop'
    if any(keyword in message_lower for keyword in greeting_keywords):
        return 'greeting'
    
    return 'default'

